void start_mic(void) {
    HAL_I2S_Receive_DMA(&hi2s1, i2s_buffer[0], WINDOWSIZE);
    active_i2s_buffer = 1;
}

void HAL_I2S_RxCpltCallback(I2S_HandleTypeDef *hi2s) {
    HAL_I2S_Receive_DMA(&hi2s1, i2s_buffer[active_i2s_buffer], WINDOWSIZE);
    active_i2s_buffer = !active_i2s_buffer;

}
